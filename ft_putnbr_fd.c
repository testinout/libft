#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	int	temp;

	temp = 0;
	if (n == -2147483648)
	{
		write(fd, "-2147483648", 11);
		return ;
	}
	if (n < 0)
	{
		write (fd, "-", 1);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		n = n % 10;
	}
	temp = n + '0';
	write(fd, &temp, 1);
}
